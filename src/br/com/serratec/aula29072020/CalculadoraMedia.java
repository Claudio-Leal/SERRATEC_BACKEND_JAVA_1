package br.com.serratec.aula29072020;

public class CalculadoraMedia {

	public static void main(String[] args) {
		double nota1 = 5.0;
		double nota2 = 6.0;
		double nota3 = 7.0;
		double nota4 = 8.0;
		double media = (nota1 + nota2 + nota3 + nota4) / 4;
		System.out.println("Média: " + media);
	}

}
